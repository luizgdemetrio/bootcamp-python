"""- Escreva um programa que receba dois números flutuantes e realize sua adição."""

num1 = float(input("Inserir primeiro número flutuante para adição: "))
num2 = float(input("Inserir segundo número flutuante para adição: "))
print(f"A soma dos números flutuantes {num1} e {num2} é: {num1 + num2}")
